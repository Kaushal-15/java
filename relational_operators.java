public class relational_operator {

    public static void main(String[] args) {
        int number1 = 12; int number2 = 15; 

        System.out.println(number1 == number2);  // is not equal to 10 // 
        System.out.println(number1 != number2); // is greater than 13 // 
        System.out.println(number1> number2); // is less than 16 // 
        System.out.println(number1 < number2);  // is greater than or equal to 19 // 
        System.out.println(number1 >= number2);  //is less than or equal to 22 
        System.out.println(number1 <= number2);
    }
}
