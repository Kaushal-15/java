public class arithmetic_operators {

    public static void main(String[] args) {
        //Implicit is happening below the conversion of smaller datatype to  larger one
        int number1 = 5;
        int number2 = 6;
        //Addition 
        System.out.println(number1 + number2);
        //subtraction
        System.out.println(number1 - number2);
        //Multiplication
        System.out.println(number1 * number2);
        //division
        System.out.println(number1 / number2);
        //Modulo
        System.out.println(number1 % number2);


        
        
    }
}
